#addistion
def addition_des_nombres(nombre1, nombre2):
    resultat = nombre1 + nombre2
    print(f"La somme de {nombre1} et de {nombre2} est : {resultat}")

    #multiplication
def multiplication_des_nombres(nombre1, nombre2):
    resultat = nombre1 * nombre2
    print(f"Le produit de {nombre1} et de {nombre2} est : {resultat}")

    #division
def division_des_nombres(nombre1, nombre2):
    resultat = nombre1 / nombre2
    print(f"Le quotient de {nombre1} et de {nombre2} est : {resultat}")
    
   #soustraction     
def soustraction_des_nombres(nombre1, nombre2):
    resultat = nombre1 - nombre2
    print(f"Le reste de {nombre1} et de {nombre2} est : {resultat}")

    